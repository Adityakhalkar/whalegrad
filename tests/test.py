# This code will print a 4x4 square pattern of stars horizontally, 
# with each row separated by a newline character. The end=" "
# argument in the print function is used to print stars on the same line with a space in between. 
# The second print() statement is used to move to the next line after completing each row.        


for i in range(5):
  
    
    for j in range(i+1):
        n = 1
        print(n+1, end= " ")
        n = n+1
        
    print()  